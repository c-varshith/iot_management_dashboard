-- =============================================================
-- TRIGGER: trg_high_sensor_alert
-- Fires AFTER every INSERT on sensor_data.
-- If the reading exceeds a predefined threshold for its type,
-- an alert row is automatically created in the alerts table.
-- Thresholds: Temperature > 45°C, Humidity > 90%,
--             Voltage > 250V, Active Power > 3500W
-- =============================================================
DROP TRIGGER IF EXISTS trg_high_sensor_alert;

DELIMITER $$

CREATE TRIGGER trg_high_sensor_alert
AFTER INSERT ON sensor_data
FOR EACH ROW
BEGIN
    DECLARE v_threshold FLOAT DEFAULT NULL;

    SET v_threshold = CASE NEW.type_id
        WHEN 1 THEN 45.0    -- Temperature threshold (°C)
        WHEN 2 THEN 90.0    -- Humidity threshold (%)
        WHEN 3 THEN 250.0   -- Voltage threshold (V)
        WHEN 4 THEN 3500.0  -- Active Power threshold (W)
        ELSE NULL
    END;

    IF v_threshold IS NOT NULL AND NEW.value > v_threshold THEN
        INSERT INTO alerts
            (device_id, type_id, alert_message, threshold_val, actual_val)
        VALUES (
            NEW.device_id,
            NEW.type_id,
            CONCAT('ALERT: Sensor type ', NEW.type_id,
                   ' on device ', NEW.device_id,
                   ' — value=', ROUND(NEW.value, 2),
                   ' exceeds threshold=', v_threshold),
            v_threshold,
            NEW.value
        );
    END IF;
END$$

DELIMITER ;

-- =============================================================
-- STORED PROCEDURE: GetSensorStats
-- Returns aggregated statistics (count, avg, min, max, stddev)
-- for a given sensor type over the last N hours.
-- Usage: CALL GetSensorStats(1, 24);  -- type_id=1 (Temp), last 24h
-- =============================================================
DROP PROCEDURE IF EXISTS GetSensorStats;

DELIMITER $$

CREATE PROCEDURE GetSensorStats(
    IN  p_type_id TINYINT,
    IN  p_hours   INT
)
BEGIN
    SELECT
        st.measurement                   AS sensor_type,
        st.unit                          AS unit,
        COUNT(sd.reading_id)             AS total_readings,
        ROUND(AVG(sd.value), 2)          AS avg_value,
        ROUND(MIN(sd.value), 2)          AS min_value,
        ROUND(MAX(sd.value), 2)          AS max_value,
        ROUND(STDDEV(sd.value), 2)       AS std_dev,
        MAX(sd.timestamp)                AS last_reading_at
    FROM sensor_data  sd
    JOIN sensor_types st ON sd.type_id = st.type_id
    WHERE sd.type_id  = p_type_id
      AND sd.timestamp >= NOW() - INTERVAL p_hours HOUR;
END$$

DELIMITER ;

