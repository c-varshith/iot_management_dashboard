-- =============================================================
-- DDL: CHECK CONSTRAINTS (Assertions)
-- MySQL 8.0.16+ enforces CHECK constraints.
-- These act as assertion-style data-integrity rules.
-- Note: SQL standard ASSERT statements are not supported by
-- MySQL; CHECK constraints serve the same purpose.
-- =============================================================
ALTER TABLE devices
    ADD CONSTRAINT chk_device_status CHECK (status IN (0, 1));

ALTER TABLE alerts
    ADD CONSTRAINT chk_alert_resolved CHECK (is_resolved IN (0, 1));

-- =============================================================
-- ADVANCED SQL: VIEW — vw_active_alerts
-- Joins alerts, devices, and sensor_types into a single
-- readable view. Queried by the application to display the
-- active alerts panel without repeating the JOIN logic.
-- =============================================================
CREATE OR REPLACE VIEW vw_active_alerts AS
SELECT
    a.alert_id,
    d.device_name,
    d.location,
    st.measurement,
    st.unit,
    a.alert_message,
    a.actual_val,
    a.threshold_val,
    a.created_at
FROM   alerts       a
JOIN   devices      d  ON a.device_id = d.device_id
JOIN   sensor_types st ON a.type_id   = st.type_id
WHERE  a.is_resolved = 0
ORDER  BY a.created_at DESC;

